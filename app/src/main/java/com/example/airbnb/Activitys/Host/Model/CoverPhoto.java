package com.example.airbnb.Activitys.Host.Model;

public class CoverPhoto {

    int img;
    String name;


    public CoverPhoto(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
